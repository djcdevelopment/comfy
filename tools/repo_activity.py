@dataclasses.dataclass
class FileActivity:
    path: str
    size_bytes: int
    category: str
    created_at: dt.datetime | None = None
    updated_at: dt.datetime | None = None
    touches: int = 0
    additions: int = 0
    deletions: int = 0
    active_days: set[str] = dataclasses.field(default_factory=set)
    recent_touches: int = 0
    score: float = 0.0

    @property
    def top_level(self) -> str:
        return self.path.split("/", 1)[0]


def run_git(*args: str) -> str:
    result = subprocess.run(
        ["git", *args], cwd=ROOT, check=True, stdout=subprocess.PIPE,
        stderr=subprocess.PIPE, text=True, encoding="utf-8", errors="replace"
    )
    return result.stdout


def load_config(path: Path = CONFIG_PATH) -> dict[str, Any]:
    return json.loads(path.read_text(encoding="utf-8"))


def parse_iso(value: str) -> dt.datetime:
    return dt.datetime.fromisoformat(value.replace("Z", "+00:00"))


def percentile(values: Iterable[float], fraction: float = 0.95) -> float:
    ordered = sorted(values)
    if not ordered:
        return 1.0
    index = min(len(ordered) - 1, max(0, math.ceil(len(ordered) * fraction) - 1))
    return max(float(ordered[index]), 1.0)


def normalized_log(value: float, cap: float) -> float:
    return min(1.0, math.log1p(max(0.0, value)) / math.log1p(max(1.0, cap)))


def category_for(path: str, config: dict[str, Any]) -> str:
    for category in config["categories"]:
        if any(path.startswith(prefix) for prefix in category["prefixes"]):
            return category["name"]
    return "Repository root"


def is_excluded(path: str, config: dict[str, Any]) -> bool:
    return any(path.startswith(prefix) for prefix in config.get("exclude_prefixes", []))


def tracked_files(config: dict[str, Any], revision: str) -> dict[str, FileActivity]:
    raw = subprocess.run(
        ["git", "ls-tree", "-r", "-l", "-z", revision], cwd=ROOT, check=True,
        stdout=subprocess.PIPE, stderr=subprocess.PIPE
    ).stdout
    files: dict[str, FileActivity] = {}
    for row in raw.split(b"\0"):
        if not row:
            continue
        metadata, raw_path = row.split(b"\t", 1)
        parts = metadata.split()
        size = 0 if parts[-1] == b"-" else int(parts[-1])
        path = raw_path.decode("utf-8", errors="replace").replace("\\", "/")
        if not is_excluded(path, config):
            files[path] = FileActivity(path, size, category_for(path, config))
    return files


def commit_records(config: dict[str, Any], files: dict[str, FileActivity], revision: str) -> list[dict[str, Any]]:
    tz = ZoneInfo(config["timezone"])
    marker = "@@COMMIT@@"
    output = run_git(
        "log", "--reverse", "--date=iso-strict", f"--format={marker}%H|%aI",
        "--numstat", "--no-renames", revision
    )
    commits: list[dict[str, Any]] = []
    current: dict[str, Any] | None = None
    for line in output.splitlines():
        if line.startswith(marker):
            sha, stamp = line[len(marker):].split("|", 1)
            current = {"sha": sha, "at": parse_iso(stamp).astimezone(tz), "files": []}
            commits.append(current)
            continue
        if not current or not line or "\t" not in line:
            continue
        additions, deletions, path = line.split("\t", 2)
        path = path.replace("\\", "/")
        if path not in files:
            continue
        add = 0 if additions == "-" else int(additions)
        delete = 0 if deletions == "-" else int(deletions)
        current["files"].append((path, add, delete))
        file = files[path]
        file.created_at = file.created_at or current["at"]
        file.updated_at = current["at"]
        file.touches += 1
        file.additions += add
        file.deletions += delete
        file.active_days.add(current["at"].date().isoformat())
    return commits


def compute_scores(config: dict[str, Any], files: dict[str, FileActivity], latest: dt.datetime) -> None:
    touched = [file for file in files.values() if file.updated_at]
    touch_cap = percentile(file.touches for file in touched)
    size_cap = percentile(file.size_bytes / 1024 for file in files.values())
    repo_start = min(file.created_at for file in touched if file.created_at)
    repo_days = max(1.0, (latest - repo_start).total_seconds() / 86400)
    recent_start = latest - dt.timedelta(days=config["recent_burst_days"])
    recent_cap = percentile(
        sum(1 for day in file.active_days if dt.date.fromisoformat(day) >= recent_start.date())
        for file in touched
    )
    weights = config["weights"]
    size_floor = config["size_weight"]["floor"]
    size_ceiling = config["size_weight"]["ceiling"]
    for file in touched:
        age_days = max(0.0, (latest - file.updated_at).total_seconds() / 86400)
        recency = 2 ** (-age_days / config["recency_half_life_days"])
        touch = normalized_log(file.touches, touch_cap)
        span_days = max(0.0, (file.updated_at - file.created_at).total_seconds() / 86400)
        lifespan = min(1.0, math.log1p(span_days) / math.log1p(repo_days))
        file.recent_touches = sum(
            1 for day in file.active_days if dt.date.fromisoformat(day) >= recent_start.date()
        )
        burst = normalized_log(file.recent_touches, recent_cap)
        size_norm = normalized_log(file.size_bytes / 1024, size_cap)
        size_weight = size_floor + (size_ceiling - size_floor) * size_norm
        base = (
            weights["recency"] * recency
            + weights["touch_frequency"] * touch
            + weights["active_lifespan"] * lifespan
            + weights["recent_burst"] * burst
        )
        file.score = round(100 * base * size_weight, 2)


def era_for(day: str, config: dict[str, Any]) -> str:
    for era in config["eras"]:
        if era["start"] <= day <= era["end"]:
            return era["id"]
    return f"later-{day[:7]}"


def report_eras(config: dict[str, Any], files: dict[str, FileActivity]) -> list[dict[str, str]]:
    eras = [dict(era) for era in config["eras"]]
    known = {era["id"] for era in eras}
    later: dict[str, set[str]] = {}
    for file in files.values():
        for stamp in (file.created_at, file.updated_at):
            if not stamp:
                continue
            day = stamp.date().isoformat()
            era_id = era_for(day, config)
            if era_id not in known:
                later.setdefault(era_id, set()).add(day)
    for era_id, days in sorted(later.items(), key=lambda item: min(item[1])):
        month = era_id.removeprefix("later-")
        eras.append({
            "id": era_id,
            "label": f"Later work ({month})",
            "start": min(days),
            "end": max(days),
        })
    return eras


def aggregate(config: dict[str, Any], files: dict[str, FileActivity], commits: list[dict[str, Any]]) -> dict[str, Any]:
    daily: dict[str, dict[str, Any]] = {}
    for commit in commits:
        day = commit["at"].date().isoformat()
        row = daily.setdefault(day, {"commits": 0, "files": set(), "touches": 0, "additions": 0, "deletions": 0, "weighted_kb": 0.0})
        row["commits"] += 1
        for path, additions, deletions in commit["files"]:
            row["files"].add(path)
            row["touches"] += 1
            row["additions"] += additions
            row["deletions"] += deletions
            row["weighted_kb"] += math.log2(files[path].size_bytes / 1024 + 1)
    for row in daily.values():
        row["files"] = len(row["files"])
        row["activity"] = round(row["commits"] + math.log1p(row["additions"] + row["deletions"]) + row["weighted_kb"], 2)

    directory: dict[str, dict[str, Any]] = {}
    category: dict[str, dict[str, Any]] = {}
    matrix: dict[tuple[str, str], dict[str, Any]] = {}
    for file in files.values():
        if not file.updated_at:
            continue
        for key, target in ((file.top_level, directory), (file.category, category)):
            row = target.setdefault(key, {"files": 0, "size_bytes": 0, "touches": 0, "additions": 0, "deletions": 0, "active_days": set(), "score": 0.0})
            row["files"] += 1
            row["size_bytes"] += file.size_bytes
            row["touches"] += file.touches
            row["additions"] += file.additions
            row["deletions"] += file.deletions
            row["active_days"].update(file.active_days)
            row["score"] += file.score
            if target is directory:
                row["is_directory"] = "/" in file.path
        created_era = era_for(file.created_at.date().isoformat(), config)
        updated_era = era_for(file.updated_at.date().isoformat(), config)
        cell = matrix.setdefault((created_era, updated_era), {"files": 0, "size_bytes": 0, "touches": 0, "score": 0.0})
        cell["files"] += 1
        cell["size_bytes"] += file.size_bytes
        cell["touches"] += file.touches
        cell["score"] += file.score

    def finish(rows: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
        result = []
        for name, row in rows.items():
            row["name"] = name
            row["active_days"] = len(row["active_days"])
            row["size_kb"] = round(row.pop("size_bytes") / 1024, 2)
            row["score"] = round(row["score"], 2)
            result.append(row)
        return sorted(result, key=lambda item: item["score"], reverse=True)

    directories = finish(directory)
    for row in directories:
        candidate = f"{row['name']}/README.md"
        row["entrypoint"] = candidate if candidate in files else None
    return {
        "daily": [{"date": day, **daily[day]} for day in sorted(daily)],
        "directories": directories,
        "categories": finish(category),
        "period_matrix": [
            {"created_era": key[0], "updated_era": key[1], "files": value["files"],
             "size_kb": round(value["size_bytes"] / 1024, 2), "touches": value["touches"],
             "score": round(value["score"], 2)}
            for key, value in sorted(matrix.items())
        ],
    }


def readme_for(directory: str) -> str | None:
    if directory.endswith(".md") or "." in Path(directory).name:
        return None
    candidate = ROOT / directory / "README.md"
    return f"../../{directory}/README.md" if candidate.exists() else None


def serialise(config: dict[str, Any], files: dict[str, FileActivity], commits: list[dict[str, Any]], summary: dict[str, Any], revision: str) -> dict[str, Any]:
    latest = max(commit["at"] for commit in commits)
    earliest = min(commit["at"] for commit in commits)
    return {
        "schema_version": 1,
        "source": {
            "commit": revision,
            "repository_url": config["repository_url"],
            "default_branch": config["default_branch"],
            "timezone": config["timezone"],
            "first_commit_at": earliest.isoformat(),
            "last_commit_at": latest.isoformat(),
            "tracked_files": len(files),
            "commits": len(commits),
            "timestamp_policy": "Git author time: first path appearance and most recent path change. Filesystem timestamps are excluded from public scoring.",
        },
        "scoring": {
            "weights": config["weights"],
            "size_weight": config["size_weight"],
            "recency_half_life_days": config["recency_half_life_days"],
            "recent_burst_days": config["recent_burst_days"],
        },
        "eras": report_eras(config, files),
        **summary,
        "files": [
            {
                "path": file.path,
                "category": file.category,
                "size_kb": round(file.size_bytes / 1024, 2),
                "repo_created_at": file.created_at.isoformat() if file.created_at else None,
                "repo_updated_at": file.updated_at.isoformat() if file.updated_at else None,
                "touches": file.touches,
                "additions": file.additions,
                "deletions": file.deletions,
                "active_days": len(file.active_days),
                "recent_touches": file.recent_touches,
                "created_era": era_for(file.created_at.date().isoformat(), config) if file.created_at else None,
                "updated_era": era_for(file.updated_at.date().isoformat(), config) if file.updated_at else None,
                "hotspot_score": file.score,
            }
            for file in sorted(files.values(), key=lambda item: (-item.score, item.path))
        ],
    }


def markdown_report(data: dict[str, Any]) -> str:
    source = data["source"]
    lines = [
        "# Repository Hotspots", "", f"> {GENERATED_NOTICE}", "",
        f"Snapshot: `{source['commit'][:12]}` · {source['commits']} commits · {source['tracked_files']} tracked files · "
        f"{source['first_commit_at'][:10]} through {source['last_commit_at'][:10]}", "",
        "[Interactive heatmap](index.html) · [GitHub-style activity graph](activity.svg) · [Raw metrics](activity.json)", "",
        "## Hotspot directories", "",
        "| Rank | Area | Score | Files | Size KB | Touches | Active days | Entry point |",
        "|---:|---|---:|---:|---:|---:|---:|---|",
    ]
    for index, row in enumerate(data["directories"], 1):
        entry = readme_for(row["name"])
        entry_text = f"[README]({entry})" if entry else "—"
        lines.append(f"| {index} | `{row['name']}` | {row['score']:.2f} | {row['files']} | {row['size_kb']:.2f} | {row['touches']} | {row['active_days']} | {entry_text} |")
    lines += ["", "## Categories", "", "| Category | Score | Files | Size KB | Touches |", "|---|---:|---:|---:|---:|"]
    for row in data["categories"]:
        lines.append(f"| {row['name']} | {row['score']:.2f} | {row['files']} | {row['size_kb']:.2f} | {row['touches']} |")
    lines += [
        "", "## How to read this", "",
        "- `repo_created_at` is the first commit in which the current path appears.",
        "- `repo_updated_at` is the most recent commit that changes the current path.",
        "- Scores combine update recency, touch frequency, active lifespan, and recent burst activity, then apply a capped logarithmic size weight.",
        "- The temporal heatmap uses first-seen era on one axis, last-updated era on the other, size for cell area, and touches for color.",
        "- Clone-local filesystem timestamps and ignored/private files do not affect these reports.", "",
        "## Periodization", "",
    ]
    for era in data["eras"]:
        lines.append(f"- **{era['start']} → {era['end']}:** {era['label']}")
    return "\n".join(lines) + "\n"


def activity_svg(data: dict[str, Any], minimum_weeks: int) -> str:
    rows = {dt.date.fromisoformat(row["date"]): row for row in data["daily"]}
    first = min(rows)
    last = max(rows)
    start = first - dt.timedelta(days=first.weekday())
    weeks = max(minimum_weeks, math.ceil(((last - start).days + 1) / 7))
    start = last - dt.timedelta(days=last.weekday()) - dt.timedelta(weeks=weeks - 1)
    values = [row["activity"] for row in rows.values()]
    cap = percentile(values)
    colors = ["#ebedf0", "#9be9a8", "#40c463", "#30a14e", "#216e39"]
    cell, gap, left, top = 11, 3, 34, 27
    width = max(320, left + weeks * (cell + gap) + 8)
    height = top + 7 * (cell + gap) + 25
    parts = [
        f'<svg xmlns="http://www.w3.org/2000/svg" width="{width}" height="{height}" viewBox="0 0 {width} {height}" role="img" aria-labelledby="title desc">',
        '<title id="title">Repository activity</title>',
        '<desc id="desc">Daily repository activity weighted by commits, churn, and current file size.</desc>',
        '<style>text{font:10px -apple-system,BlinkMacSystemFont,Segoe UI,sans-serif;fill:#57606a}.month{font-size:9px}</style>',
        '<rect width="100%" height="100%" fill="white"/>',
        '<text x="0" y="49">Mon</text><text x="0" y="77">Wed</text><text x="0" y="105">Fri</text>',
    ]
    last_month = None
    for week in range(weeks):
        week_date = start + dt.timedelta(weeks=week)
        if week_date.month != last_month:
            parts.append(f'<text class="month" x="{left + week * (cell + gap)}" y="12">{week_date.strftime("%b")}</text>')
            last_month = week_date.month
        for weekday in range(7):
            day = week_date + dt.timedelta(days=weekday)
            row = rows.get(day)
            value = row["activity"] if row else 0
            level = 0 if not row else min(4, max(1, math.ceil(value / cap * 4)))
            x, y = left + week * (cell + gap), top + weekday * (cell + gap)
            detail = "No activity" if not row else f"{row['commits']} commits, {row['files']} files, +{row['additions']}/-{row['deletions']}"
            parts.append(f'<rect x="{x}" y="{y}" width="{cell}" height="{cell}" rx="2" fill="{colors[level]}"><title>{day.isoformat()}: {html.escape(detail)}</title></rect>')
    parts.append(f'<text x="{left}" y="{height - 5}">Weighted activity · snapshot {data["source"]["commit"][:12]}</text>')
    parts.append("</svg>")
    return "\n".join(parts) + "\n"


