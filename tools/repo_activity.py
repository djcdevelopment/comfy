def local_timestamps(files: dict[str, FileActivity]) -> dict[str, Any]:
    result = {}
    for path in files:
        candidate = ROOT / path
        if candidate.exists():
            stat = candidate.stat()
            result[path] = {
                "working_copy_created_at": dt.datetime.fromtimestamp(stat.st_ctime, dt.timezone.utc).isoformat(),
                "working_copy_updated_at": dt.datetime.fromtimestamp(stat.st_mtime, dt.timezone.utc).isoformat(),
                "warning": "Machine-local filesystem evidence; excluded from hotspot scoring.",
            }
    return result


def root_activity_block(data: dict[str, Any]) -> str:
    lines = [
        ROOT_MARKER_START,
        "## Repository activity map",
        "",
        "The map below is generated from Git history: first-seen date × last-updated date, weighted by current KB and change activity.",
        "",
        "[Explore the interactive heatmap](docs/repo-map/index.html) · [Read the hotspot report](docs/repo-map/HOTSPOTS.md) · [Inspect the raw metrics](docs/repo-map/activity.json)",
        "",
        "![GitHub-style repository activity graph](docs/repo-map/activity.svg)",
        "",
        "### Current hotspots",
        "",
        "| Area | Activity score | Files | Size KB | Start here |",
        "|---|---:|---:|---:|---|",
    ]
    for row in data["directories"]:
        readme = ROOT / row["name"] / "README.md"
        if readme.exists():
            lines.append(f"| `{row['name']}` | {row['score']:.2f} | {row['files']} | {row['size_kb']:.2f} | [README]({row['name']}/README.md) |")
    lines += ["", f"Metrics snapshot: `{data['source']['commit'][:12]}`. Regenerate with `python tools/repo_activity.py --write`.", ROOT_MARKER_END]
    return "\n".join(lines)


def replace_root_activity(readme: str, data: dict[str, Any]) -> str:
    if ROOT_MARKER_START not in readme or ROOT_MARKER_END not in readme:
        return readme
    before, remainder = readme.split(ROOT_MARKER_START, 1)
    _, after = remainder.split(ROOT_MARKER_END, 1)
    return before + root_activity_block(data) + after


def expected_outputs(config: dict[str, Any], data: dict[str, Any]) -> dict[Path, str]:
    outputs = {
        ROOT / config["outputs"]["json"]: json.dumps(data, indent=2, ensure_ascii=False) + "\n",
        ROOT / config["outputs"]["markdown"]: markdown_report(data),
        ROOT / config["outputs"]["svg"]: activity_svg(data, config["history_weeks_minimum"]),
        ROOT / config["outputs"]["html"]: interactive_html(data),
    }
    root_readme = ROOT / "README.md"
    if root_readme.exists():
        current = root_readme.read_text(encoding="utf-8")
        updated = replace_root_activity(current, data)
        if updated != current or ROOT_MARKER_START in current:
            outputs[root_readme] = updated
    return outputs


def build(config: dict[str, Any], revision: str | None = None) -> tuple[dict[str, Any], dict[str, FileActivity]]:
    revision = run_git("rev-parse", revision or "HEAD").strip()
    files = tracked_files(config, revision)
    commits = commit_records(config, files, revision)
    if not commits:
        raise RuntimeError("No commits found")
    latest = max(commit["at"] for commit in commits)
    compute_scores(config, files, latest)
    summary = aggregate(config, files, commits)
    return serialise(config, files, commits, summary, revision), files


def is_managed_publication_path(path: str) -> bool:
    path = path.replace("\\", "/")
    return path.startswith("docs/repo-map/") or path in MANAGED_PUBLICATION_PATHS


def recorded_revision() -> str | None:
    activity_path = ROOT / load_config()["outputs"]["json"]
    if not activity_path.exists():
        return None
    try:
        return json.loads(activity_path.read_text(encoding="utf-8"))["source"]["commit"]
    except (KeyError, TypeError, json.JSONDecodeError):
        return None


def publication_only_since(revision: str) -> tuple[bool, list[str]]:
    ancestor = subprocess.run(
        ["git", "merge-base", "--is-ancestor", revision, "HEAD"], cwd=ROOT
    ).returncode == 0
    if not ancestor:
        return False, ["recorded source is not an ancestor of HEAD"]
    changed = [line for line in run_git("diff", "--name-only", f"{revision}..HEAD").splitlines() if line]
    unexpected = [path for path in changed if not is_managed_publication_path(path)]
    return not unexpected, unexpected


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    mode = parser.add_mutually_exclusive_group(required=True)
    mode.add_argument("--write", action="store_true", help="write the committed reports")
    mode.add_argument("--check", action="store_true", help="verify reports match HEAD")
    mode.add_argument("--local-timestamps", metavar="PATH", help="write optional machine-local timestamps")
    args = parser.parse_args(argv)
    config = load_config()
    check_revision = recorded_revision() if args.check else None
    data, files = build(config, check_revision)
    if args.local_timestamps:
        target = Path(args.local_timestamps)
        target.write_text(json.dumps(local_timestamps(files), indent=2) + "\n", encoding="utf-8")
        print(f"Wrote local timestamp diagnostics to {target}")
        return 0
    outputs = expected_outputs(config, data)
    if args.write:
        for path, content in outputs.items():
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(content, encoding="utf-8", newline="\n")
            print(path.relative_to(ROOT))
        return 0
    stale = []
    for path, content in outputs.items():
        if not path.exists() or path.read_text(encoding="utf-8") != content:
            stale.append(str(path.relative_to(ROOT)))
    if stale:
        print("Stale repository activity outputs:", file=sys.stderr)
        print("\n".join(f"  {path}" for path in stale), file=sys.stderr)
        return 1
    publication_ok, unexpected = publication_only_since(data["source"]["commit"])
    if not publication_ok:
        print("Repository activity snapshot is behind non-publication changes:", file=sys.stderr)
        print("\n".join(f"  {path}" for path in unexpected), file=sys.stderr)
        return 1
    print(f"Repository activity outputs match recorded snapshot {data['source']['commit'][:12]}.")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
